function slugExt(contentType, url) {
  if (contentType?.includes('pdf') || url.toLowerCase().endsWith('.pdf')) return 'pdf';
  if (contentType?.includes('csv') || url.toLowerCase().endsWith('.csv')) return 'csv';
  if (contentType?.includes('markdown') || url.toLowerCase().endsWith('.md')) return 'md';
  if (contentType?.includes('plain') || url.toLowerCase().endsWith('.txt')) return 'txt';
  return 'html';
}

function stripHtmlTitle(html) {
  const match = html.match(/<title[^>]*>(.*?)<\/title>/is);
  return (match?.[1] ?? '').replace(/\s+/g, ' ').trim();
}

function isBlockedOrErrorHtml(html) {
  const title = stripHtmlTitle(html);
  const normalized = normalizeWhitespace(html).toLowerCase();
  return (
    /error:\s*the request could not be satisfied/i.test(title) ||
    normalized.includes('error: the request could not be satisfied') ||
    normalized.includes('generated by cloudfront') ||
    normalized.includes('<h1>403 error</h1>')
  );
}

