async function fetchCdcAzTopicSeeds(existingSources) {
  const downloaded = await downloadSource({
    id: 'cdc-health-topics-a-z-index',
    url: 'https://www.cdc.gov/az/az-export.json',
    authority: 'CDC',
    title: 'CDC Health Topics A-Z Index',
  });

  const knownUrls = new Set(
    existingSources.flatMap((source) =>
      [source.url, source.finalUrl].map((value) => normalizeComparableUrl(value)).filter(Boolean),
    ),
  );
  const seenUrls = new Set();
  const items = JSON.parse(Buffer.from(downloaded.bytes).toString('utf8'));
  const seeds = [];

  for (const item of items) {
    const title = stripHtmlTags(item.title);
    const url = normalizeComparableUrl(item.url);
    const haystack = `${title} ${url}`.toLowerCase();
    if (!url.startsWith('https://www.cdc.gov/')) {
      continue;
    }
    if (!cdcAzTopicPatterns.some((pattern) => pattern.test(haystack))) {
      continue;
    }
    if (knownUrls.has(url) || seenUrls.has(url)) {
      continue;
    }

    const urlPath = new URL(url).pathname.replace(/(^\/|\/$)/g, '').replace(/\/+/g, '-');
    const sourceId = `cdc-az-${slugifySourceId(urlPath || title || `topic-${seeds.length + 1}`)}`;
    seeds.push({
      id: sourceId,
      url,
      authority: 'CDC',
      title: title || `CDC Topic ${seeds.length + 1}`,
    });
    seenUrls.add(url);
  }

  seeds.sort((left, right) => left.title.localeCompare(right.title));
  return cdcAzTopicLimit > 0 ? seeds.slice(0, cdcAzTopicLimit) : seeds;
}

async function mapWithConcurrency(items, limit, worker) {
  const results = [];
  let cursor = 0;

  async function runWorker() {
    while (cursor < items.length) {
      const index = cursor;
      cursor += 1;
      results[index] = await worker(items[index], index);
    }
  }

  const workers = Array.from({ length: Math.min(limit, items.length) }, () => runWorker());
  await Promise.all(workers);
  return results;
}

async function extractPdfPages(rawPath) {
  const { stdout } = await execFile(knowledgePython, [pdfExtractor, rawPath], {
    maxBuffer: 1024 * 1024 * 64,
  });
  const parsed = JSON.parse(stdout);
  return (parsed.pages ?? [])
    .map((page) => ({
      page: page.page,
      text: normalizeWhitespace(page.text ?? ''),
    }))
    .filter((page) => page.text.length >= 120);
}

function buildPdfChunkEntries(source, pages) {
  const chunks = [];
  for (const page of pages) {
    const paragraphs = page.text
      .split(/\n{2,}/)
      .map((part) => normalizeWhitespace(part))
      .filter((part) => part.length >= 50);
    for (const chunk of chunkParagraphs(paragraphs)) {
      chunks.push(`Page ${page.page}. ${chunk}`);
    }
  }
  return buildChunkEntries(source, chunks, '全文手册');
}

function extractMainHtml(html) {
  const mainMatch = html.match(/<main\b[^>]*>([\s\S]*?)<\/main>/i);
  if (!mainMatch) {
    return html;
  }

  return mainMatch[1]
    .replace(/<nav[\s\S]*?<\/nav>/gi, ' ')
    .replace(/<footer[\s\S]*?<\/footer>/gi, ' ');
}

function buildHtmlChunkEntries(source, html) {
  const title = stripHtmlTitle(html);
  const plain = stripHtml(extractMainHtml(html));
  const paragraphs = plain
    .split(/\n{2,}/)
    .map((part) => normalizeWhitespace(part))
    .filter(
      (part) =>
        part.length >= 60 &&
        !/^cookie|^menu|^search$/i.test(part) &&
        !/^(msd manual|merck manual|consumer version|professional edition)$/i.test(part) &&
        !/^(skip to|open menu close menu|an official website of the united states government)/i.test(part) &&
        !/^(accessibility privacy policy|foia notices|vulnerability disclosure policy|usa\.gov)/i.test(part) &&
        !/^(national park service search search this site|explore this park|loading alerts alerts in effect)/i.test(part) &&
        !/here's how you know official websites use \.gov/i.test(part),
    );
  const chunks = chunkParagraphs(paragraphs.length > 0 ? paragraphs : [plain]);
  const effectiveSource = title ? { ...source, title } : source;
  return buildChunkEntries(effectiveSource, chunks, '全文网页');
}

function buildTextChunkEntries(source, text) {
  const paragraphs = text
    .replace(/\r/g, '\n')
    .split(/\n{2,}/)
    .map((part) => normalizeWhitespace(part))
    .filter((part) => part.length >= 60);
  return buildChunkEntries(source, chunkParagraphs(paragraphs), '全文文档');
}

function buildCsvChunkEntries(source, csvText) {
  const rows = parseCsv(csvText);
  const priority = entryPriorityForSource(source);
  const severity = severityForPriority(priority);
  const tags = dedupeStrings([source.authority, source.title, source.id, 'dataset', 'csv', ...sourceTerms(source)]);

  return rows
    .map((row, index) => {
      const question = cleanDatasetField(row.question ?? row.Question ?? row.prompt ?? '');
      const answer = cleanDatasetField(row.answer ?? row.Answer ?? row.response ?? '');
      const texts = [question, ...extractDatasetTexts(answer)].map((item) => normalizeWhitespace(item)).filter(Boolean);
      const joined = texts.join(' ');
      if (joined.length < 40) {
        return null;
      }

      const summary = question || texts[0] || `Dataset row ${index + 1}`;
      const steps = extractDatasetSteps(answer);
      return {
        id: `${source.id}-row-${String(index + 1).padStart(4, '0')}`,
        title: `${source.title} Q${index + 1} ${summary.slice(0, 60)}`,
        summary,
        steps: steps.length > 0 ? steps : texts.slice(1, 5),
        contraindications: [],
        escalation: `Dataset source row ${index + 1} from ${source.title}`,
        tags,
        aliases: [question, source.title, source.authority].filter(Boolean),
        source: `${source.authority}: ${source.title}`,
        sourceId: source.id,
        sourceUrl: source.finalUrl,
        priority,
        category: '数据集知识块',
        locale: 'en',
        severity,
      };
    })
    .filter(Boolean);
}

