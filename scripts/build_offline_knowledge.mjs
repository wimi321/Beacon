function severityForPriority(priority) {
  if (priority >= 10) return 'critical';
  if (priority >= 7) return 'high';
  return 'medium';
}

function summarizeChunk(text) {
  const sentences = splitSentences(text);
  const summary = normalizeWhitespace(sentences[0] ?? text).slice(0, 180);
  const steps = dedupeStrings(sentences.slice(1, 5).map((item) => normalizeWhitespace(item))).slice(0, 4);
  return {
    summary: summary || 'See source excerpt.',
    steps: steps.length > 0 ? steps : [normalizeWhitespace(text).slice(0, 280)],
  };
}

function chunkParagraphs(paragraphs, maxChars = 1100) {
  const chunks = [];
  let current = [];
  let currentLength = 0;

  for (const paragraph of paragraphs) {
    const text = normalizeWhitespace(paragraph);
    if (!text) {
      continue;
    }

    if (text.length > maxChars * 1.25) {
      const sentences = splitSentences(text);
      let sentenceBucket = [];
      let sentenceLength = 0;
      for (const sentence of sentences) {
        if (sentenceLength + sentence.length > maxChars && sentenceBucket.length > 0) {
          chunks.push(sentenceBucket.join(' '));
          sentenceBucket = [];
          sentenceLength = 0;
        }
        sentenceBucket.push(sentence);
        sentenceLength += sentence.length + 1;
      }
      if (sentenceBucket.length > 0) {
        chunks.push(sentenceBucket.join(' '));
      }
      continue;
    }

    if (currentLength + text.length > maxChars && current.length > 0) {
      chunks.push(current.join('\n\n'));
      current = [];
      currentLength = 0;
    }

    current.push(text);
    currentLength += text.length + 2;
  }

  if (current.length > 0) {
    chunks.push(current.join('\n\n'));
  }

  return chunks.map((chunk) => normalizeWhitespace(chunk)).filter((chunk) => chunk.length >= 80);
}

function buildChunkEntries(source, chunks, category = '全文知识块') {
  const priority = entryPriorityForSource(source);
  const severity = severityForPriority(priority);
  const topicProfile = sourceTopicProfile(source);
  const effectiveCategory = category.startsWith('全文') && topicProfile.category ? topicProfile.category : category;
  const tags = dedupeStrings([effectiveCategory, ...topicProfile.tags, source.authority, source.title, ...sourceTerms(source)]);
  const aliases = dedupeStrings([source.title, source.authority, source.id, ...topicProfile.tags]).slice(0, 12);

  return chunks.map((chunk, index) => {
    const { summary, steps } = summarizeChunk(chunk);
    const titleSeed = splitSentences(chunk)[0] ?? summary;
    const title = `${source.title} #${index + 1} ${normalizeWhitespace(titleSeed).slice(0, 48)}`.trim();
    return {
      id: `${source.id}-chunk-${String(index + 1).padStart(4, '0')}`,
      title,
      summary,
      steps,
      contraindications: [],
      escalation: `原始来源：${source.authority} / ${source.title}`,
      tags,
      aliases,
      source: `${source.authority}: ${source.title}`,
      sourceId: source.id,
      sourceUrl: source.finalUrl,
      priority,
      category: effectiveCategory,
      locale: 'en',
      severity,
    };
  });
}

function applySourceTopicProfile(entry, source) {
  const topicProfile = source ? sourceTopicProfile(source) : { tags: [], category: null, priorityFloor: null };
  const category =
    topicProfile.category && /^全文/.test(entry.category)
      ? topicProfile.category
      : entry.category;
  const priority =
    topicProfile.priorityFloor != null
      ? Math.max(entry.priority, topicProfile.priorityFloor)
      : entry.priority;

  return {
    ...entry,
    category,
    tags: dedupeStrings([category, ...entry.tags, ...topicProfile.tags]),
    aliases: dedupeStrings([...entry.aliases, ...topicProfile.tags]).slice(0, 16),
    sourceUrl: entry.sourceUrl || source?.finalUrl || source?.url || '',
    priority,
    severity: severityForPriority(priority),
  };
}

function parseCsv(text) {
  const rows = [];
  let current = '';
  let row = [];
  let inQuotes = false;

  for (let index = 0; index < text.length; index += 1) {
    const char = text[index];
    const next = text[index + 1];

    if (char === '"') {
      if (inQuotes && next === '"') {
        current += '"';
        index += 1;
      } else {
        inQuotes = !inQuotes;
      }
      continue;
    }

    if (char === ',' && !inQuotes) {
      row.push(current);
      current = '';
      continue;
    }

    if ((char === '\n' || char === '\r') && !inQuotes) {
      if (char === '\r' && next === '\n') {
        index += 1;
      }
      row.push(current);
      current = '';
      if (row.some((cell) => cell.length > 0)) {
        rows.push(row);
      }
      row = [];
      continue;
    }

    current += char;
  }

  if (current.length > 0 || row.length > 0) {
    row.push(current);
    rows.push(row);
  }

  const [header = [], ...dataRows] = rows;
  return dataRows.map((cells) => Object.fromEntries(header.map((key, idx) => [key, cells[idx] ?? ''])));
}

function maybeParseJsonArray(value) {
  const trimmed = value.trim();
  if (!trimmed.startsWith('[') || !trimmed.endsWith(']')) {
    return null;
  }

  try {
    const parsed = JSON.parse(trimmed);
    return Array.isArray(parsed) ? parsed.map((item) => `${item}`) : null;
  } catch {
    return null;
  }
}

function cleanDatasetField(value) {
  return value
    .replace(/^"+|"+$/g, '')
    .replace(/^Answer:\s*/i, '')
    .replace(/\\"/g, '"')
    .trim();
}

function extractDatasetTexts(value) {
  const cleaned = cleanDatasetField(value);
  const parsed = maybeParseJsonArray(cleaned);
  if (parsed) {
    return parsed.map((item) => cleanDatasetField(item));
  }
  return [cleaned];
}

function extractDatasetSteps(answer) {
  const texts = extractDatasetTexts(answer);
  const lines = [];

  for (const text of texts) {
    for (const segment of splitSentences(text)) {
      const cleaned = segment
        .replace(/^[\-\u2022*]\s*/, '')
        .replace(/^\d+[.)]\s*/, '')
        .replace(/^If\s+/i, '')
        .replace(/^Then\s+/i, '')
        .trim();

      if (!cleaned || cleaned.length < 12) {
        continue;
      }

      lines.push(cleaned);
    }
  }

  return dedupeStrings(lines).slice(0, 4);
}

async function downloadWithCurl(item) {
  const tempDir = await mkdtemp(path.join(os.tmpdir(), 'beacon-knowledge-'));
  const tempFile = path.join(tempDir, item.id);
  try {
    const { stdout } = await execFile('curl', [
      '-L',
      '-sS',
      '--compressed',
      '--retry',
      '3',
      '--retry-all-errors',
      '--retry-delay',
      '2',
      '-A',
      browserUserAgent,
      '-H',
      'accept: text/html,text/plain,text/markdown,text/csv,application/pdf;q=0.9,*/*;q=0.8',
      '-H',
      'accept-language: en-US,en;q=0.9',
      '-o',
      tempFile,
      '-w',
      '%{content_type}\n%{url_effective}',
      item.url,
    ]);

    const [contentType = '', finalUrl = item.url] = stdout.trim().split('\n');
    const bytes = new Uint8Array(await readFile(tempFile));
    return { bytes, contentType, finalUrl };
  } finally {
    await rm(tempDir, { recursive: true, force: true });
  }
}

async function downloadSource(item) {
  for (let attempt = 1; attempt <= merckTopicRetries; attempt += 1) {
    try {
      const controller = new AbortController();
      const timeout = setTimeout(() => controller.abort(), 30_000);
      const response = await fetch(item.url, {
        headers: {
          'user-agent': browserUserAgent,
          accept: 'text/html,text/plain,text/markdown,text/csv,application/pdf;q=0.9,*/*;q=0.8',
          'accept-language': 'en-US,en;q=0.9',
        },
        redirect: 'follow',
        signal: controller.signal,
      }).finally(() => clearTimeout(timeout));

      if (!response.ok) {
        throw new Error(`HTTP ${response.status} ${response.statusText}`);
      }

      return {
        bytes: new Uint8Array(await response.arrayBuffer()),
        contentType: response.headers.get('content-type') ?? '',
        finalUrl: response.url,
      };
    } catch (error) {
      if (attempt === merckTopicRetries) {
        return downloadWithCurl(item);
      }
      await new Promise((resolve) => setTimeout(resolve, 500 * attempt));
    }
  }
}

async function fetchSitemapTopicUrls(sitemap) {
  const xml = await downloadSource({
    id: sitemap.sitemapId,
    url: sitemap.sitemapUrl,
    authority: sitemap.authority,
    title: sitemap.titlePrefix,
  });
  const text = Buffer.from(xml.bytes).toString('utf8');
  const matches = Array.from(text.matchAll(/<loc>(.*?)<\/loc>/g)).map((match) => match[1]);
  const urls = matches
    .filter((url) => url.startsWith(sitemap.prefix))
    .map((url) => `${url}${url.includes('?') ? '&' : '?'}media=print`);
  return merckTopicLimit > 0 ? urls.slice(0, merckTopicLimit) : urls;
}

